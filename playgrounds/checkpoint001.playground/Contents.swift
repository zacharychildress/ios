import Cocoa

//Convert Celsius to Fahrenheit
//1. Create a constant holding any temp in Celsius
//2. Convert it to Fahrenheit by multiplying by 9, dividing by 5, then adding 32
//3. Print the results for the user, showing both the Celsius and Fahrenheit values

let celsius = 30.0

let fahrenheit = celsius * 9 / 5 + 32
print("The original temp in Celsius is: \(celsius). The temp converted to Fahrenheit is \(fahrenheit).")
